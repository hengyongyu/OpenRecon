nElem = 100;
if (strcmp(Name, 'Compound Names'))
  P = Props(nElem+1:end, 3:5);
  return;
end
if (strcmp(Name, 'Element Data'))
  P = Props(1:nElem,[3, 1]);
  return;
end
if (strcmp(Name, 'All Data'))
  P = Props(:,[1 2 5 4 3]);
  idx = find(cellfun('isempty',P(:, 3)));
  P(idx, 3) = P(idx, 5);
  return;
end

%% Process Material Input Parameter
if (ischar   (Name)), Name = {Name}; end
if (isnumeric(Name)), Name = num2cell(Name); end

%% Initialize variables
nName = length(Name);
nData = length(Props);
P     = cell(nName,size(Props, 2));

%% Main Loop
for i = 1:nName
  name = Name{i};
  if (ischar(name)) 
    k = find(strcmpi( name, Props(:, 3:4)), 1);% which benchmark?
    if (isempty(k))
      k = find(strcmp( name, Props(:, 5)), 1);% which benchmark?
    end;
    name = nData;
    if (~isempty(k)), name=k; end;
    if (name>nData), name = name-nData; end
  end
  P(i,:) = Props(name,:);
  if (~isempty(P{i, 5})), P(i, 3) = P(i, 5); end
end
